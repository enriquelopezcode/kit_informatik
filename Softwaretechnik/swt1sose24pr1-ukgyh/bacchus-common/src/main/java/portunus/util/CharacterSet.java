package portunus.util;

/**
 * An enum specifying different character sets.
 */
public enum CharacterSet {
    /**
     * All upper case letters.
     */
    UPPER_CASE,

    /**
     * All lower case letters.
     */
    LOWER_CASE,

    /**
     * All digits.
     */
    DIGITS,

    /**
     * The minus sign '-'.
     */
    MINUS,

    /**
     * The underscore sign '_'.
     */
    UNDERSCORE,

    /**
     * The space sign ' '.
     */
    SPACE
}
