package edu.kit.orlog.exceptions;

/**
 * This exception is thrown when the starting arguments are invalid.
 * @author ukgyh
 */
public class StartingArgumentInvalidException extends Exception {

    /**
     * Constructs a new exception with the specified detail message.
     * @param message the detail message
     */
    public StartingArgumentInvalidException(String message) {
        super(message);
    }


}
